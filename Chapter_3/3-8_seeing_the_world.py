places = ['Africa','Israel','Italy','Texas','Egypt']
print(places)

print(sorted(places))
print(places)

# Use sorted() to print your list in reverse alphabetical order without changing the order of the original list.
print(sorted(places, reverse=True))
print(places)

places.reverse()
print(places)

places.reverse()
print(places)

places.sort()
print(places)

places.sort(reverse=True)
print(places)