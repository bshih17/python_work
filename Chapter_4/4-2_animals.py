animals = 'caterpillar', 'tadpole'
for animal in animals:
	print(f"The {animal} must go through metamorphosis to reach its intended design. It must go through metamorphosis to reach maturity, completion, and adulthood.")

print("\nMetamorphosis is a 'change in form' or 'tranformation'.\n")

adults = 'butterfly', 'frog'
for adult in adults:
	print(f"The {adult} is the final product after going through metamorphosis.")

print("\nInsects and amphibians can go through metamorphosis. They are amazing creatures.")